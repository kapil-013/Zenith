import { collection, addDoc, getDocs, deleteDoc, doc, query } from "firebase/firestore";
import { db } from "./firebase";
import { Issue } from "../types";

export const clearSeedData = async () => {
  console.log("Clearing existing seed data from issues, verifications, status_updates, notifications...");
  const collectionsToClear = ["issues", "verifications", "status_updates", "notifications"];
  for (const colName of collectionsToClear) {
    try {
      const qSnapshot = await getDocs(collection(db, colName));
      for (const docSnapshot of qSnapshot.docs) {
        await deleteDoc(doc(db, colName, docSnapshot.id));
      }
      console.log(`Cleared collection: ${colName}`);
    } catch (e) {
      console.error(`Error clearing collection ${colName}:`, e);
    }
  }
};

