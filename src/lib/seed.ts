export const loadDemoData = async () => {
  // Clear all existing seed data completely first
  await clearSeedData();

  console.log("Seeding 20 new high-quality realistic issues...");
  for (const issue of demoIssues) {
    const docRef = await addDoc(collection(db, "issues"), issue);
    
    // Create status updates
    let note = `System initialized issue with status ${issue.status}.`;
    if (issue.status === "Resolved" || issue.status === "Confirmed") {
      note = `Department resolved this issue: The repair crew completed all necessary physical repairs and restored the area to normal.`;
    } else if (issue.status === "In Progress") {
      note = `Department officer has been dispatched to the site to inspect and initiate repair work.`;
    } else if (issue.status === "Verified") {
      note = `Community residents have verified this report as an active, genuine civic concern.`;
    }
    
    await addDoc(collection(db, "status_updates"), {
        issueId: docRef.id,
        status: issue.status,
        note: note,
        updatedBy: "demo-admin",
        createdAt: issue.updatedAt || Date.now()
    });
  }
  console.log("Seeded 20 demo issues successfully.");
};

export const resetDemoData = async () => {
  // Reload demo data clears and seeds fresh
  await loadDemoData();
};

